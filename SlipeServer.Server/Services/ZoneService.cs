    public string GetZoneName(Vector3 position, bool citiesOnly = false)
    {
        string? bestMatch = null;
        float bestVolume = float.MaxValue;

        foreach (var zone in zones)
        {
            if (citiesOnly && !cityNames.Contains(zone.Name))
                continue;

            if (Vector3.Clamp(position, zone.Min, zone.Max) == position)
            {
                var size = zone.Max - zone.Min;
                float volume = size.X * size.Y * size.Z;
                if (volume < bestVolume)
                {
                    bestVolume = volume;
                    bestMatch = zone.Name;
                }
            }
        }

        return bestMatch ?? (citiesOnly ? "Countryside" : "San Andreas");
    }
